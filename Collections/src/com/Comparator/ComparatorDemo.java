package com.Comparator;

public class ComparatorDemo
{
	int age;
	String name;
	int rollno;
	ComparatorDemo(int age,String name, int rollno)
	{
	this.age=age;
	this.name=name;
	this.rollno=rollno;
	}
	@Override
	public String toString() {
		return "ComparatorDemo [age=" + age + ", name=" + name + ", rollno=" + rollno + "]";
	}
	
	
	
}
